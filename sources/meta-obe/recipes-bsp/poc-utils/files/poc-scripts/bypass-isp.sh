# write to expander on 0x34 address (no driver loaded) to enable power for AP1302

# reset
i2cset -y 2 0x34 0x23 0x00
i2cset -y 2 0x34 0x24 0x00
i2cset -y 2 0x34 0x27 0x00
i2cset -y 2 0x34 0x28 0x00

i2cdetect -y 2

# GPIO8 - Power BUCK
i2cset -y 2 0x34 0x24 0x02
i2cset -y 2 0x34 0x28 0x02

i2cdetect -y 2
# GPIO1 - DVDD sel (switch to 1V2)
i2cset -y 2 0x34 0x23 0x01
i2cset -y 2 0x34 0x27 0x01

i2cdetect -y 2
# GPIO7 - VDIO enable
i2cset -y 2 0x34 0x24 0x03
i2cset -y 2 0x34 0x28 0x03

i2cdetect -y 2
# GPIO2 - VAA SEL
i2cset -y 2 0x34 0x23 0x03
i2cset -y 2 0x34 0x27 0x03

i2cdetect -y 2
# GPIO9 - VAA (PWREN3)
i2cset -y 2 0x34 0x24 0x07
i2cset -y 2 0x34 0x28 0x07

i2cdetect -y 2
# GPIO5 (VCM Power Enable)
i2cset -y 2 0x34 0x23 0x13
i2cset -y 2 0x34 0x27 0x13

i2cdetect -y 2
